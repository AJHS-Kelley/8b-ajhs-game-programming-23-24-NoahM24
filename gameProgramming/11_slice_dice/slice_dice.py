alphabet = "abcdefghijklmnopqrstuvwxyz"
my_string = "This is my string which consists of letters and words. L"

print(alphabet[0])
print(alphabet[-1])

# Slicing
print(alphabet[0:4]) # Slice UP TO second index
# alphabet[start:stop]

# Slice from the Start
print(alphabet[:7])

# Slice to the End
print(alphabet[12:])

# Slice the Whole %#$#! Thing
print(alphabet[:])

# Negative Index Slicing
print(alphabet[-4:-1])

# Messing Around
print(my_string[24:])